package com.mvest.m.bank;

public class Account {
	String ac_id;
	String bk_id;
	String bk_name;
	String ac_balance;
	String ac_stock;
	String ac_date;
	
	public String getAc_id() {
		return ac_id;
	}
	public void setAc_id(String ac_id) {
		this.ac_id = ac_id;
	}
	public String getBk_id() {
		return bk_id;
	}
	public void setBk_id(String bk_id) {
		this.bk_id = bk_id;
	}
	public String getBk_name() {
		return bk_name;
	}
	public void setBk_name(String bk_name) {
		this.bk_name = bk_name;
	}
	public String getAc_balance() {
		return ac_balance;
	}
	public void setAc_balance(String ac_balance) {
		this.ac_balance = ac_balance;
	}
	public String getAc_stock() {
		return ac_stock;
	}
	public void setAc_stock(String ac_stock) {
		this.ac_stock = ac_stock;
	}
	public String getAc_date() {
		return ac_date;
	}
	public void setAc_date(String ac_date) {
		this.ac_date = ac_date;
	}
	
	
	
	
}
